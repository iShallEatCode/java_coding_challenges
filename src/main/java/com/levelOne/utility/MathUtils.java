package com.levelOne.utility;

// The 'MathUtils' class is a utility class containing static methods for mathematical operations.
// The 'add()', 'subtract()', and 'multiply()' methods performs operation of two numbers and returns the result.
// As these methods are static, you can call them directly on the class without needing to create an instance of 'MathUtils'.

public class MathUtils {
    // Static method for addition
    public static int add(int num1, int num2) {
        return num1 + num2;
    }
    // Static method for subtraction
    public static int subtract(int num1, int num2) {
        return num1 - num2;
    }
    // Static method for multiplication
    public static int multiply(int num1, int num2) {
        return num1 * num2;
    }
    
}
